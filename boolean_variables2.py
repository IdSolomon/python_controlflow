# Creating a variable named myBabyBool
# Then setting it equal to "true"

myBabyBool = "true"

# Checking the type of myBabyBool using type(myBabyBool)

type(myBabyBool)
print(type(myBabyBool))

# This shows that "true" is not a boolean variable, but a string!
# Boolean values True and False always need to be capitalized and do not have quotation marks.

# So, creating another variable named myBabyBoolTwo and setting it equal to True

myBabyBoolTwo = True

# Checking the type once more with type()

type(myBabyBoolTwo)
print(type(myBabyBoolTwo))

# The result is the class 'bool' rather than 'str'