# Setting a variable equal to a boolean expression

bool_one = 5 != 7
bool_two = 1 + 1 != 2
bool_three = 3 * 3 == 9

# These variables now contain boolean values. 
# When I try and reference them, they will only return True or False values.

print(bool_one)
print(bool_two)
print(bool_three)