# Understanding boolean variables and expressions is essential
# because they are the building blocks of conditional statements.

# Here is one conditional statement --

if 8 == 10 - 2:
    print("orange")

# This code will print 'orange' because the condition of the if statement
# '8 == 10 - 2' is True.

# Here is another example --

playerOne = "Scott"

if playerOne == "Scott":
    print("Ready, Player One!")

# If playerOne is Scott, it'll tell him the system is ready for him!

playerOne = "Troy"

if playerOne == "Troy":
    print("Ready, Player One! Saved Game.")

# If playerOne is Troy, it'll tell him the system is ready with his saved game.
