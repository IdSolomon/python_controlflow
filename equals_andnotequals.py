# A boolean expression can be created by using relational operators

# Relational operators compare two items and return either True or False

# They are sometimes called comparators

# The equals (==) and not equals (!=) relational operators compare two items
# and return True or False.

firstStatement = 1 == 1
print(firstStatement)

secondStatement = 2 != 4
print(secondStatement)

thirdStatement = 3 == 5
print(thirdStatement)

fourthStatement = '7' == 7
print(fourthStatement)

# Here are three more statements --

fifthStatement = (5 * 2) - 1 == 8 + 1
print(fifthStatement)

sixthStatement = 13 - 6 != (3 * 2) + 1
print(sixthStatement)

seventhStatement = 3 * (2 - 1) == 4 - 1
print(seventhStatement)
